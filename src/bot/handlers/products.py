def register_handlers(bot):

    @bot.message_handler(func=lambda m: m.text == "‚ûï –î–æ–±–∞–≤–∏—Ç—å —Ç–æ–≤–∞—Ä")
    def add_product(message: Message):
        user_states[message.chat.id] = "waiting_for_product_url"
        bot.send_message(message.chat.id, "–û—Ç–ø—Ä–∞–≤—å —Å—Å—ã–ª–∫—É –Ω–∞ —Ç–æ–≤–∞—Ä (–Ω–∞–ø—Ä–∏–º–µ—Ä —Å Ozon).")

    @bot.message_handler(func=lambda m: m.text == "‚ûñ –£–¥–∞–ª–∏—Ç—å —Ç–æ–≤–∞—Ä")
    def remove_product(message: Message):
        bot.send_message(message.chat.id, "–í—ã–±–µ—Ä–∏ —Ç–æ–≤–∞—Ä –¥–ª—è —É–¥–∞–ª–µ–Ω–∏—è (–ø–æ–∫–∞ –∑–∞–≥–ª—É—à–∫–∞).")

    @bot.message_handler(func=lambda m: m.text == "üìã –ú–æ–∏ —Ç–æ–≤–∞—Ä—ã")
    def list_products(message: Message):
        bot.send_message(message.chat.id, "–¢–≤–æ–∏ –æ—Ç—Å–ª–µ–∂–∏–≤–∞–µ–º—ã–µ —Ç–æ–≤–∞—Ä—ã (–ø–æ–∫–∞ –∑–∞–≥–ª—É—à–∫–∞).")

    # –æ–±—Ä–∞–±–æ—Ç–∫–∞ —Å—Å—ã–ª–∫–∏ –Ω–∞ —Ç–æ–≤–∞—Ä
    @bot.message_handler(func=lambda m: user_states.get(m.chat.id) == 'waiting_for_product_url')
    def handle_product_url(message: Message):
        url = message.text.strip()
        user_id = str(message.from_user.id)

        # –ü–∞—Ä—Å–∏–Ω–≥ –¥–∞–Ω–Ω—ã—Ö
        try:
            bot.send_message(message.chat.id, f'–ü—Ä–æ—Ü–µ—Å—Å –ø–∞—Ä—Å–∏–Ω–≥–∞ –¥–∞–Ω–Ω—ã—Ö –Ω–∞—á–∞—Ç, –æ–∂–∏–¥–∞–π—Ç–µ.')
            product_data = parser.parse_product(url)
            logger.info(f'–£—Å–ø–µ—à–Ω—ã–π –ø–∞—Ä—Å–∏–Ω–≥ –¥–∞–Ω–Ω—ã—Ö –¥–ª—è {url}: {product_data}')
        except Exception as e:
            bot.send_message(message.chat.id, f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–∞—Ä—Å–∏–Ω–≥–µ: {str(e)}. –ü—Ä–æ–≤–µ—Ä—å —Å—Å—ã–ª–∫—É.", reply_markup=main_menu())
            logger.error(f"–û—à–∏–±–∫–∞ –ø–∞—Ä—Å–∏–Ω–≥–∞ URL {url} –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è {user_id}: {str(e)}")
            return
        
        # –°–æ–∑–¥–∞–Ω–∏–µ –¥–æ–º–µ–Ω–Ω—ã—Ö —Å—É—â–Ω–æ—Å—Ç–µ–π
        try:
            product = Product (
                id=product_data['id'],
                user_id=user_id,
                price_id=str(uuid.uuid4()),
                name=product_data['name'],
                link=url,
                image_url=product_data['image_url'],
                rating=product_data['rating'],
                categories=product_data['categories']
            )

            price = Price(
                id=product.price_id,
                product_id=product.id,
                with_card=product_data['price_with_card'],
                without_card=product_data['price_without_card'],
                previous_with_card=product_data['price_default'], # –ü–†–û–ë–õ–ï–ú–ö–ê
                previous_without_card=product_data['price_default'],  # –ü–†–û–ë–õ–ï–ú–ö–ê
                default=product_data['price_default'],
                claim=datetime.now(),
            )
            user = User(
                id=user_id,
                username=message.from_user.username,
                chat_id=user_id,
                products=[] # –ê –∫–∞–∫ –∑–¥–µ—Å—å –¥–æ–±–∞–≤–∏—Ç—å –≤ —Å–ø–∏—Å–æ–∫ –µ—â–µ –æ–¥–∏–Ω —Ç–æ–≤–∞—Ä???
            )
            
        except Exception as e:
            bot.send_message(message.chat.id, f"–û—à–∏–±–∫–∞ —Å–æ–∑–¥–∞–Ω–∏—è —Å—É—â–Ω–æ—Å—Ç–µ–π: {str(e)}.", reply_markup=main_menu())
            logger.error(f"–û—à–∏–±–∫–∞ —Å–æ–∑–¥–∞–Ω–∏—è —Å—É—â–Ω–æ—Å—Ç–µ–π –¥–ª—è {url} –æ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è {user_id}: {str(e)}")
            return
        
        try:
            product_service.create_user()
            product_service.create_product(user_id, product, price)
            bot.send_message(message.chat.id, f"–¢–æ–≤–∞—Ä {product_data['name']} —É—Å–ø–µ—à–Ω–æ –¥–æ–±–∞–≤–ª–µ–Ω!", reply_markup=main_menu())
            logger.info(f"–î–∞–Ω–Ω—ã–µ –¥–ª—è {url} —Å–æ—Ö—Ä–∞–Ω–µ–Ω—ã –≤ –ë–î –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è {user_id}")
        except Exception as e:
            bot.send_message(message.chat.id, f"–û—à–∏–±–∫–∞ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è –≤ –ë–î: {str(e)}. –î–∞–Ω–Ω—ã–µ –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω—ã, –Ω–æ –Ω–µ —Å–æ—Ö—Ä–∞–Ω–µ–Ω—ã.", reply_markup=main_menu())
            logger.error(f"–û—à–∏–±–∫–∞ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è –≤ –ë–î –¥–ª—è {url} –æ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è {user_id}: {str(e)}")

        # –°–±—Ä–æ—Å —Å–æ—Å—Ç–æ—è–Ω–∏—è
        del user_states[message.chat.id]