                chat_id=int(chat_id),
                text=text,
                parse_mode='Markdown',  # –£–∫–∞–∑—ã–≤–∞–µ–º, —á—Ç–æ —Ñ–æ—Ä–º–∞—Ç –±—É–¥–µ—Ç Markdown
